The templates files, within the views directory, contain mainly markup language (HTML) that can allow the user to enter data from the browser (which can be processed by a controller). These templates can also determines what output data is visible on each page in the browser, and also structures how this data is seen. These templates also have markers that allow the aesthetics of what is shown on each page to be controlled via a style language (CSS).
