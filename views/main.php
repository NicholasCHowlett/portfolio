Within the project directory there are individual directories which each contain auxiliary files, main (controller) files, and template files.
The auxiliary files, within the includes directory, contain programming language (PHP) code that configures various server settings, and additionally code which is packaged into individual packages (called functions) which allows a particular block of code to be reused in differing parts of the application (e.g. on each page) more efficiently.
